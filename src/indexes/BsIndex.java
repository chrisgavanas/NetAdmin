package indexes;

import java.util.ArrayList;
import java.util.HashMap;

import Interfaces.BaseStationWrapper;

public class BsIndex {
	// This class is Index for the data of the base_station file it contains a
	// Navigable map,a List,and HashMap of the the data for easy access and
	// use//
	// ------------------------------------------------------------------------------------------------------------------------------------//
	private String Key;
	private HashMap<String, ArrayList<BaseStationWrapper>> BsHm;
	private ArrayList<BaseStationWrapper> BsAl;

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public BsIndex(String key, ArrayList<BaseStationWrapper> BsAl, boolean hm) {
		setKey(key);
		setBsAl(BsAl);

		if (!hm)
			return;
		else
			BsHm = new HashMap<String, ArrayList<BaseStationWrapper>>();

		ArrayList<BaseStationWrapper> hashlist;
		for (BaseStationWrapper bw : BsAl) {
			hashlist = BsHm.get(bw.get(key));
			if (hashlist == null) {
				hashlist = new ArrayList<BaseStationWrapper>();
				hashlist.add(bw);
				BsHm.put(bw.get(key), hashlist);
			} else {
				hashlist.add(bw);
				BsHm.put(bw.get(key), hashlist);
			}
		}
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public String getKey() {
		return Key;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setKey(String key) {
		Key = key;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public HashMap<String, ArrayList<BaseStationWrapper>> getBsHm() {
		return BsHm;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setBsHm(HashMap<String, ArrayList<BaseStationWrapper>> bsHm) {
		BsHm = bsHm;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public ArrayList<BaseStationWrapper> getBsAl() {
		return BsAl;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setBsAl(ArrayList<BaseStationWrapper> bsAl) {
		BsAl = bsAl;
	}
	// ------------------------------------------------------------------------------------------------------------------------------------//

}