package indexes;

import java.util.ArrayList;
import java.util.HashMap;

import Interfaces.BatteryWrapper;

public class BatIndex {
	// This class is Index for the data of the battery file it contains a
	// Navigable map,a List,and HashMap of the the data for easy access and
	// use//
	// ------------------------------------------------------------------------------------------------------------------------------------//
	private String Key;
	private HashMap<String, ArrayList<BatteryWrapper>> BatHm;
	private ArrayList<BatteryWrapper> BatAl;

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public BatIndex(String key, ArrayList<BatteryWrapper> BatAl, boolean hm) {
		setKey(key);
		setBatAl(BatAl);
		if (!hm)
			return;
		else
			BatHm = new HashMap<String, ArrayList<BatteryWrapper>>();

		// --Setting the comparator--//

		ArrayList<BatteryWrapper> hashlist;
		for (BatteryWrapper bw : BatAl) {
			hashlist = BatHm.get(bw.get(key));
			if (hashlist == null) {
				hashlist = new ArrayList<BatteryWrapper>();
				hashlist.add(bw);
				BatHm.put(bw.get(key), hashlist);
			} else {
				hashlist.add(bw);
				BatHm.put(bw.get(key), hashlist);
			}
		}
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public String getKey() {
		return Key;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setKey(String key) {
		Key = key;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public HashMap<String, ArrayList<BatteryWrapper>> getBatHm() {
		return BatHm;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setBatHm(HashMap<String, ArrayList<BatteryWrapper>> batHm) {
		BatHm = batHm;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public ArrayList<BatteryWrapper> getBatAl() {
		return BatAl;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setBatAl(ArrayList<BatteryWrapper> batAl) {
		BatAl = batAl;
	}
	// ------------------------------------------------------------------------------------------------------------------------------------//
}