package indexes;

import java.util.ArrayList;
import java.util.HashMap;

import Interfaces.WifiWrapper;

public class WifIndex {
	// This class is Index for the data of the wifi file it contains a Navigable
	// map,a List,and HashMap of the the data for easy access and use//
	// ------------------------------------------------------------------------------------------------------------------------------------//
	private String Key;
	private HashMap<String, ArrayList<WifiWrapper>> WifHm;
	private ArrayList<WifiWrapper> WifAl;

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public WifIndex(String key, ArrayList<WifiWrapper> WifAl, boolean hm) {
		setKey(key);
		setWifAl(WifAl);
		if (!hm)
			return;
		else
			WifHm = new HashMap<String, ArrayList<WifiWrapper>>();

		ArrayList<WifiWrapper> hashlist;
		for (WifiWrapper bw : WifAl) {
			hashlist = WifHm.get(bw.get(key));
			if (hashlist == null) {
				hashlist = new ArrayList<WifiWrapper>();
				hashlist.add(bw);
				WifHm.put(bw.get(key), hashlist);
			} else {
				hashlist.add(bw);
				WifHm.put(bw.get(key), hashlist);
			}
		}
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public String getKey() {
		return Key;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setKey(String key) {
		Key = key;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public HashMap<String, ArrayList<WifiWrapper>> getWifHm() {
		return WifHm;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setWifHm(HashMap<String, ArrayList<WifiWrapper>> wifHm) {
		WifHm = wifHm;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public ArrayList<WifiWrapper> getWifAl() {
		return WifAl;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setWifAl(ArrayList<WifiWrapper> wifAl) {
		WifAl = wifAl;
	}
	// ------------------------------------------------------------------------------------------------------------------------------------//
}