package indexes;

import java.util.ArrayList;
import java.util.HashMap;

import Interfaces.GpsWrapper;

public class GpsIndex {
	// This class is Index for the data of the gps file it contains a Navigable
	// map,a List,and HashMap of the the data for easy access and use//
	// ------------------------------------------------------------------------------------------------------------------------------------//
	private String Key;
	private HashMap<String, ArrayList<GpsWrapper>> GpsHm;
	private ArrayList<GpsWrapper> GpsAl;

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public GpsIndex(String key, ArrayList<GpsWrapper> GpsAl, boolean hm) {
		setKey(key);
		setGpsAl(GpsAl);
		if (!hm)
			return;
		else
			GpsHm = new HashMap<String, ArrayList<GpsWrapper>>();

		ArrayList<GpsWrapper> hashlist;
		for (GpsWrapper bw : GpsAl) {
			hashlist = GpsHm.get(bw.get(key));
			if (hashlist == null) {
				hashlist = new ArrayList<GpsWrapper>();
				hashlist.add(bw);
				GpsHm.put(bw.get(key), hashlist);
			} else {
				hashlist.add(bw);
				GpsHm.put(bw.get(key), hashlist);
			}
		}
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public String getKey() {
		return Key;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setKey(String key) {
		Key = key;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public ArrayList<GpsWrapper> getGpsAl() {
		return GpsAl;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setGpsAl(ArrayList<GpsWrapper> gpsAl) {
		GpsAl = gpsAl;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public HashMap<String, ArrayList<GpsWrapper>> getGpsHm() {
		return GpsHm;
	}

	// ------------------------------------------------------------------------------------------------------------------------------------//

	// ------------------------------------------------------------------------------------------------------------------------------------//
	public void setGpsHm(HashMap<String, ArrayList<GpsWrapper>> gpsHm) {
		GpsHm = gpsHm;
	}
	// ------------------------------------------------------------------------------------------------------------------------------------//
}